import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { PNG } from 'pngjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');
const sourcePath = resolve(repoRoot, 'public/assets/empress.png');
const outputDir = resolve(repoRoot, 'public/assets/generated');
const manifestPath = resolve(repoRoot, 'src/generated/empressAtlasManifest.js');

const CHARACTER_SKIN_COUNT = 32;
const CHARACTER_SHEET = {
  frameSize: 64,
  gap: 1,
  leftOffset: 1,
  rowLightThreshold: 20,
  cellLightThreshold: 100,
  cellBackground: [147, 187, 236],
  extensionBackground: [58, 111, 51],
  sheetBackground: [27, 89, 153],
  maxExtension: 96,
};
const CHARACTER_PALETTE_GRID = {
  x: 1,
  y: 2606,
  columns: 16,
  rows: 2,
  stepX: 39,
  stepY: 59,
  previewWidth: 37,
  previewHeight: 46,
  swatchLeftPadding: 1,
  swatchTopGap: 3,
  swatchSize: 4,
  swatchCount: 9,
};
const CHARACTER_PALETTE_PREVIEW_CELLS = Array.from({ length: CHARACTER_SKIN_COUNT }, (_entry, index) => ({
  x: CHARACTER_PALETTE_GRID.x + (index % CHARACTER_PALETTE_GRID.columns) * CHARACTER_PALETTE_GRID.stepX,
  y: CHARACTER_PALETTE_GRID.y + Math.floor(index / CHARACTER_PALETTE_GRID.columns) * CHARACTER_PALETTE_GRID.stepY,
  width: CHARACTER_PALETTE_GRID.previewWidth,
  height: CHARACTER_PALETTE_GRID.previewHeight,
  swatchX:
    CHARACTER_PALETTE_GRID.x +
    (index % CHARACTER_PALETTE_GRID.columns) * CHARACTER_PALETTE_GRID.stepX +
    CHARACTER_PALETTE_GRID.swatchLeftPadding,
  swatchY:
    CHARACTER_PALETTE_GRID.y +
    Math.floor(index / CHARACTER_PALETTE_GRID.columns) * CHARACTER_PALETTE_GRID.stepY +
    CHARACTER_PALETTE_GRID.previewHeight +
    CHARACTER_PALETTE_GRID.swatchTopGap,
  swatchSize: CHARACTER_PALETTE_GRID.swatchSize,
  swatchCount: CHARACTER_PALETTE_GRID.swatchCount,
}));

const ATLAS_MAX_WIDTH = 2048;
const ATLAS_SPACING = 1;
const ATLAS_PADDING = CHARACTER_SHEET.maxExtension;
const ATLAS_FRAME_SIZE = CHARACTER_SHEET.frameSize + ATLAS_PADDING * 2;

async function main() {
  await mkdir(outputDir, { recursive: true });
  await mkdir(dirname(manifestPath), { recursive: true });

  const source = PNG.sync.read(await readFile(sourcePath));
  const pixels = {
    width: source.width,
    height: source.height,
    data: source.data,
  };
  const geometry = detectSheetFrameGeometry(source, CHARACTER_SHEET, pixels);
  const paletteCells = detectCharacterPaletteCells(source, CHARACTER_SHEET, pixels);
  const paletteMaps = buildCharacterPaletteMaps(CHARACTER_SHEET, pixels, paletteCells);
  const skins = [];
  const placements = packAtlasFrames(geometry.frameCells);

  for (let skinIndex = 0; skinIndex < paletteMaps.length; skinIndex += 1) {
    const paletteMap = paletteMaps[skinIndex] ?? new Map();
    const skinName = `empress-skin-${String(skinIndex).padStart(2, '0')}`;
    const imageFilename = `${skinName}.png`;
    const jsonFilename = `${skinName}.json`;
    const png = renderAtlas(source, CHARACTER_SHEET, geometry.frameCells, placements, paletteMap);
    const atlasJson = buildAtlasJson(imageFilename, geometry.frameCells, placements, png.width, png.height);
    await writeFile(resolve(outputDir, imageFilename), PNG.sync.write(png));
    await writeFile(resolve(outputDir, jsonFilename), `${JSON.stringify(atlasJson, null, 2)}\n`, 'utf8');
    skins.push({
      index: skinIndex,
      image: `assets/generated/${imageFilename}`,
      json: `assets/generated/${jsonFilename}`,
    });
  }

  const manifest = {
    source: 'assets/empress.png',
    frameCount: geometry.frameCells.length,
    sourceFrameWidth: ATLAS_FRAME_SIZE,
    sourceFrameHeight: ATLAS_FRAME_SIZE,
    maxAtlasWidth: ATLAS_MAX_WIDTH,
    spacing: ATLAS_SPACING,
    padding: ATLAS_PADDING,
    skinCount: skins.length,
    skins,
  };
  await writeFile(
    manifestPath,
    `// Generated by scripts/build-character-atlases.js. Do not edit by hand.\n` +
      `export const EMPRESS_ATLAS_MANIFEST = ${JSON.stringify(manifest, null, 2)};\n` +
      `export default EMPRESS_ATLAS_MANIFEST;\n`,
    'utf8',
  );

  console.log(
    `Built ${skins.length} empress skin atlases: ${geometry.frameCells.length} frames, ` +
      `${ATLAS_FRAME_SIZE}x${ATLAS_FRAME_SIZE} logical frames.`,
  );
}

function packAtlasFrames(cells) {
  const placements = [];
  let x = 0;
  let y = 0;
  let rowHeight = 0;
  let width = 0;

  for (const cell of cells) {
    const render = cell.render;
    if (x > 0 && x + render.width > ATLAS_MAX_WIDTH) {
      y += rowHeight + ATLAS_SPACING;
      x = 0;
      rowHeight = 0;
    }

    placements.push({ x, y, width: render.width, height: render.height });
    width = Math.max(width, x + render.width);
    rowHeight = Math.max(rowHeight, render.height);
    x += render.width + ATLAS_SPACING;
  }

  return {
    width,
    height: y + rowHeight,
    frames: placements,
  };
}

function renderAtlas(source, sheet, cells, placements, paletteMap) {
  const png = new PNG({
    width: placements.width,
    height: placements.height,
    colorType: 6,
  });
  png.data.fill(0);

  for (let index = 0; index < cells.length; index += 1) {
    drawFrameToAtlas(source, sheet, cells[index], placements.frames[index], png, paletteMap);
  }

  return png;
}

function buildAtlasJson(imageFilename, cells, placements, width, height) {
  const frames = {};
  for (let index = 0; index < cells.length; index += 1) {
    const frameNumber = index + 1;
    const cell = cells[index];
    const placement = placements.frames[index];
    frames[String(frameNumber)] = {
      frame: {
        x: placement.x,
        y: placement.y,
        w: placement.width,
        h: placement.height,
      },
      rotated: false,
      trimmed: true,
      spriteSourceSize: {
        x: ATLAS_PADDING - cell.render.originX,
        y: ATLAS_PADDING - cell.render.originY,
        w: cell.render.width,
        h: cell.render.height,
      },
      sourceSize: {
        w: ATLAS_FRAME_SIZE,
        h: ATLAS_FRAME_SIZE,
      },
    };
  }

  return {
    frames,
    meta: {
      app: 'superfighters-browser',
      version: '1',
      image: imageFilename,
      format: 'RGBA8888',
      size: { w: width, h: height },
      scale: '1',
    },
  };
}

function drawFrameToAtlas(source, sheet, cell, placement, target, paletteMap) {
  const render = cell.render;

  for (let y = 0; y < render.height; y += 1) {
    const sourceY = render.y + y;
    const outY = placement.y + y;
    if (outY < 0 || outY >= target.height) {
      continue;
    }
    for (let x = 0; x < render.width; x += 1) {
      const sourceX = render.x + x;
      const outX = placement.x + x;
      if (outX < 0 || outX >= target.width) {
        continue;
      }
      const sourceIndex = (sourceY * source.width + sourceX) * 4;
      if (isTransparentAssetPixel(sheet, source.data, sourceIndex)) {
        continue;
      }

      const targetIndex = (outY * target.width + outX) * 4;
      const replacement = paletteMap.get(
        rgbKey(source.data[sourceIndex], source.data[sourceIndex + 1], source.data[sourceIndex + 2]),
      );
      target.data[targetIndex] = replacement?.[0] ?? source.data[sourceIndex];
      target.data[targetIndex + 1] = replacement?.[1] ?? source.data[sourceIndex + 1];
      target.data[targetIndex + 2] = replacement?.[2] ?? source.data[sourceIndex + 2];
      target.data[targetIndex + 3] = source.data[sourceIndex + 3];
    }
  }
}

function detectSheetFrameGeometry(sourceImage, sheet, pixels) {
  const rowStarts = detectAssetRowStarts(sourceImage, sheet, pixels);
  const cells = [];
  let detectedColumns = 0;

  for (let row = 0; row < rowStarts.length; row += 1) {
    const y = rowStarts[row];
    const rowCells = detectFrameCellsForRow(sourceImage, sheet, pixels, row, y);
    detectedColumns = Math.max(detectedColumns, rowCells.length);
    cells.push(...rowCells);
  }

  return {
    columns: detectedColumns,
    rows: rowStarts.length,
    rowStarts,
    frameCells: cells.map((cell) => ({
      ...cell,
      render: detectFrameRenderRect(sourceImage, sheet, pixels, cell),
    })),
  };
}

function detectFrameCellsForRow(sourceImage, sheet, pixels, row, y) {
  const cells = [];
  const size = sheet.frameSize;
  let x = sheet.leftOffset ?? 0;

  while (x + size <= sourceImage.width) {
    while (x + size <= sourceImage.width && !isFrameStartCandidate(sourceImage, sheet, pixels, x, y)) {
      x += 1;
    }

    if (x + size > sourceImage.width) {
      break;
    }

    cells.push({ row, column: cells.length, x, y });
    x += size;

    while (x < sourceImage.width && stripHasExtensionBackground(sheet, pixels, x, y, 1, size)) {
      x += 1;
    }
    while (x < sourceImage.width && isSheetBackgroundPixel(sheet, pixels, x, y)) {
      x += 1;
    }
  }

  return cells;
}

function isFrameStartCandidate(sourceImage, sheet, pixels, x, y) {
  if (x > 0 && !isSheetBackgroundPixel(sheet, pixels, x - 1, y)) {
    return false;
  }
  if (!isAssetCellBackgroundPixel(sheet, pixels, x, y)) {
    return false;
  }
  if (countAssetBackgroundPixelsInRect(sheet, pixels, x, y, sheet.frameSize, 1) < Math.floor(sheet.frameSize * 0.35)) {
    return false;
  }

  const rightEdge = x + sheet.frameSize;
  if (
    rightEdge < sourceImage.width &&
    !isSheetBackgroundPixel(sheet, pixels, rightEdge, y) &&
    !isExtensionBackgroundPixel(sheet, pixels, rightEdge, y)
  ) {
    return false;
  }

  return countAssetBackgroundPixelsInRect(sheet, pixels, x, y, sheet.frameSize, sheet.frameSize) >= sheet.cellLightThreshold;
}

function detectFrameRenderRect(sourceImage, sheet, pixels, cell) {
  const size = sheet.frameSize;
  const maxExtension = sheet.maxExtension ?? size;
  const base = {
    left: cell.x,
    top: cell.y,
    right: cell.x + size,
    bottom: cell.y + size,
  };
  const bounds = { ...base };

  expandHorizontal(sourceImage, sheet, bounds, pixels, 1, maxExtension);
  expandHorizontal(sourceImage, sheet, bounds, pixels, -1, maxExtension);
  expandVertical(sourceImage, sheet, bounds, pixels, 1, maxExtension);
  expandVertical(sourceImage, sheet, bounds, pixels, -1, maxExtension);

  return {
    x: bounds.left,
    y: bounds.top,
    width: bounds.right - bounds.left,
    height: bounds.bottom - bounds.top,
    originX: base.left - bounds.left,
    originY: base.top - bounds.top,
  };
}

function expandHorizontal(sourceImage, sheet, bounds, pixels, direction, maxExtension) {
  let hasExtension = false;
  for (let step = 0; step < maxExtension; step += 1) {
    const x = direction > 0 ? bounds.right : bounds.left - 1;
    if (x < 0 || x >= sourceImage.width) {
      return;
    }

    if (stripHasExtensionBackground(sheet, pixels, x, bounds.top, 1, bounds.bottom - bounds.top)) {
      if (direction > 0) {
        bounds.right += 1;
      } else {
        bounds.left -= 1;
      }
      hasExtension = true;
      continue;
    }

    if (hasExtension && stripHasSpritePixels(sheet, pixels, x, bounds.top, 1, bounds.bottom - bounds.top)) {
      if (direction > 0) {
        bounds.right += 1;
      } else {
        bounds.left -= 1;
      }
      continue;
    }

    return;
  }
}

function expandVertical(sourceImage, sheet, bounds, pixels, direction, maxExtension) {
  let hasExtension = false;
  for (let step = 0; step < maxExtension; step += 1) {
    const y = direction > 0 ? bounds.bottom : bounds.top - 1;
    if (y < 0 || y >= sourceImage.height) {
      return;
    }

    if (stripHasExtensionBackground(sheet, pixels, bounds.left, y, bounds.right - bounds.left, 1)) {
      if (direction > 0) {
        bounds.bottom += 1;
      } else {
        bounds.top -= 1;
      }
      hasExtension = true;
      continue;
    }

    if (hasExtension && stripHasSpritePixels(sheet, pixels, bounds.left, y, bounds.right - bounds.left, 1)) {
      if (direction > 0) {
        bounds.bottom += 1;
      } else {
        bounds.top -= 1;
      }
      continue;
    }

    return;
  }
}

function detectAssetRowStarts(sourceImage, sheet, pixels) {
  const lightRows = [];
  for (let y = 0; y < sourceImage.height; y += 1) {
    let lightCount = 0;
    for (let x = 0; x < sourceImage.width; x += 1) {
      if (isAssetCellBackgroundPixel(sheet, pixels, x, y)) {
        lightCount += 1;
      }
    }
    if (lightCount >= sheet.rowLightThreshold) {
      lightRows.push(y);
    }
  }

  return collapseContiguousRuns(lightRows)
    .filter((run) => run.length >= Math.max(8, sheet.frameSize * 0.75))
    .map((run) => run.start);
}

function collapseContiguousRuns(values) {
  if (!values.length) {
    return [];
  }

  const runs = [];
  let start = values[0];
  let previous = values[0];
  for (const value of values.slice(1)) {
    if (value === previous + 1) {
      previous = value;
      continue;
    }
    runs.push({ start, length: previous - start + 1 });
    start = value;
    previous = value;
  }
  runs.push({ start, length: previous - start + 1 });
  return runs;
}

function detectCharacterPaletteCells(sourceImage, sheet, pixels) {
  const fixedCells = detectFixedCharacterPaletteCells(pixels);
  if (fixedCells.length >= 2) {
    return fixedCells;
  }

  const scanTop = Math.max(0, sourceImage.height - 180);
  const scanRight = Math.min(sourceImage.width, 760);
  const scanHeight = sourceImage.height - scanTop;
  const visited = new Uint8Array(scanRight * scanHeight);
  const components = [];
  const localIndex = (x, y) => (y - scanTop) * scanRight + x;

  for (let y = scanTop; y < sourceImage.height; y += 1) {
    for (let x = 0; x < scanRight; x += 1) {
      const startIndex = localIndex(x, y);
      if (visited[startIndex] || !isPaletteSpritePixel(sheet, pixels, x, y)) {
        continue;
      }

      const stack = [[x, y]];
      visited[startIndex] = 1;
      let left = x;
      let right = x;
      let top = y;
      let bottom = y;
      let count = 0;

      while (stack.length) {
        const [currentX, currentY] = stack.pop();
        count += 1;
        left = Math.min(left, currentX);
        right = Math.max(right, currentX);
        top = Math.min(top, currentY);
        bottom = Math.max(bottom, currentY);

        for (const [nextX, nextY] of [
          [currentX + 1, currentY],
          [currentX - 1, currentY],
          [currentX, currentY + 1],
          [currentX, currentY - 1],
        ]) {
          if (nextX < 0 || nextX >= scanRight || nextY < scanTop || nextY >= sourceImage.height) {
            continue;
          }
          const nextIndex = localIndex(nextX, nextY);
          if (visited[nextIndex] || !isPaletteSpritePixel(sheet, pixels, nextX, nextY)) {
            continue;
          }
          visited[nextIndex] = 1;
          stack.push([nextX, nextY]);
        }
      }

      const width = right - left + 1;
      const height = bottom - top + 1;
      if (width >= 30 && width <= 46 && height >= 38 && height <= 56 && count >= 500) {
        components.push({ x: left, y: top, width, height });
      }
    }
  }

  const cells = components
    .sort((left, right) => left.y - right.y || left.x - right.x)
    .slice(0, CHARACTER_SKIN_COUNT)
    .map((component) => ({
      x: component.x,
      y: component.y,
      width: component.width,
      height: Math.min(58, sourceImage.height - component.y),
    }));

  return cells.length >= 2 ? cells : CHARACTER_PALETTE_PREVIEW_CELLS;
}

function detectFixedCharacterPaletteCells(pixels) {
  const cells = [];
  for (let index = 0; index < CHARACTER_SKIN_COUNT; index += 1) {
    const cell = CHARACTER_PALETTE_PREVIEW_CELLS[index];
    if (
      !cell ||
      cell.x + cell.width > pixels.width ||
      cell.y + cell.height > pixels.height ||
      cell.swatchX + cell.swatchCount * cell.swatchSize > pixels.width ||
      cell.swatchY + cell.swatchSize > pixels.height
    ) {
      continue;
    }
    if (countSpritePixelsInRect(CHARACTER_SHEET, pixels, cell.x, cell.y, cell.width, cell.height) >= 180) {
      cells.push({ ...cell });
    }
  }
  return cells;
}

function buildCharacterPaletteMaps(sheet, pixels, paletteCells) {
  if (!paletteCells.length) {
    return [new Map()];
  }

  const baseCell = paletteCells[0];
  const baseRuns = extractPaletteColorRuns(sheet, pixels, baseCell);
  return paletteCells.map((targetCell) => {
    const targetRuns = extractPaletteColorRuns(sheet, pixels, targetCell);
    const stripMap = buildPaletteMapFromColorRuns(baseRuns, targetRuns);
    const diffMap = buildPaletteMapFromSpriteDiff(sheet, pixels, baseCell, targetCell, stripMap);
    if (diffMap.size >= 3) {
      return diffMap;
    }

    const replacementCounts = new Map();
    const width = Math.min(baseCell.width, targetCell.width);
    const height = Math.min(baseCell.height, targetCell.height);
    for (let y = 0; y < height; y += 1) {
      for (let x = 0; x < width; x += 1) {
        const baseX = baseCell.x + x;
        const baseY = baseCell.y + y;
        const targetX = targetCell.x + x;
        const targetY = targetCell.y + y;
        if (!isPaletteSpritePixel(sheet, pixels, baseX, baseY) || !isPaletteSpritePixel(sheet, pixels, targetX, targetY)) {
          continue;
        }
        const baseIndex = (baseY * pixels.width + baseX) * 4;
        const targetIndex = (targetY * pixels.width + targetX) * 4;
        const sourceKey = rgbKey(pixels.data[baseIndex], pixels.data[baseIndex + 1], pixels.data[baseIndex + 2]);
        const targetKey = rgbKey(pixels.data[targetIndex], pixels.data[targetIndex + 1], pixels.data[targetIndex + 2]);
        if (!replacementCounts.has(sourceKey)) {
          replacementCounts.set(sourceKey, new Map());
        }
        const targetCounts = replacementCounts.get(sourceKey);
        const entry = targetCounts.get(targetKey) ?? {
          color: [pixels.data[targetIndex], pixels.data[targetIndex + 1], pixels.data[targetIndex + 2]],
          count: 0,
        };
        entry.count += 1;
        targetCounts.set(targetKey, entry);
      }
    }

    const paletteMap = new Map();
    for (const [sourceKey, targetCounts] of replacementCounts.entries()) {
      let best = null;
      for (const entry of targetCounts.values()) {
        if (!best || entry.count > best.count) {
          best = entry;
        }
      }
      if (best) {
        paletteMap.set(sourceKey, best.color);
      }
    }
    return paletteMap;
  });
}

function extractPaletteColorRuns(sheet, pixels, cell) {
  const runs = [];
  const swatchSize = cell.swatchSize ?? CHARACTER_PALETTE_GRID.swatchSize;
  const swatchCount = cell.swatchCount ?? CHARACTER_PALETTE_GRID.swatchCount;
  const swatchX = cell.swatchX ?? cell.x + CHARACTER_PALETTE_GRID.swatchLeftPadding;
  const swatchY = cell.swatchY ?? cell.y + cell.height + CHARACTER_PALETTE_GRID.swatchTopGap;

  for (let swatch = 0; swatch < swatchCount; swatch += 1) {
    const x = swatchX + swatch * swatchSize;
    const y = swatchY;
    const key = getDominantPaletteColorKeyInRect(sheet, pixels, x, y, swatchSize, swatchSize);
    if (!key) {
      continue;
    }
    runs.push({
      x,
      y,
      width: swatchSize,
      height: swatchSize,
      color: parseRgbKey(key),
      key,
      swatch,
    });
  }

  return runs;
}

function buildPaletteMapFromSpriteDiff(sheet, pixels, baseCell, targetCell, seedMap = new Map()) {
  const replacementCounts = new Map();
  const width = Math.min(baseCell.width, targetCell.width);
  const height = Math.min(baseCell.height, targetCell.height);
  for (let y = 0; y < height; y += 1) {
    for (let x = 0; x < width; x += 1) {
      const baseX = baseCell.x + x;
      const baseY = baseCell.y + y;
      const targetX = targetCell.x + x;
      const targetY = targetCell.y + y;
      if (!isPaletteSpritePixel(sheet, pixels, baseX, baseY) || !isPaletteSpritePixel(sheet, pixels, targetX, targetY)) {
        continue;
      }
      const baseIndex = (baseY * pixels.width + baseX) * 4;
      const targetIndex = (targetY * pixels.width + targetX) * 4;
      const sourceKey = rgbKey(pixels.data[baseIndex], pixels.data[baseIndex + 1], pixels.data[baseIndex + 2]);
      const targetKey = rgbKey(pixels.data[targetIndex], pixels.data[targetIndex + 1], pixels.data[targetIndex + 2]);
      if (sourceKey === targetKey || seedMap.has(sourceKey)) {
        continue;
      }
      if (!replacementCounts.has(sourceKey)) {
        replacementCounts.set(sourceKey, new Map());
      }
      const targetCounts = replacementCounts.get(sourceKey);
      const entry = targetCounts.get(targetKey) ?? {
        color: [pixels.data[targetIndex], pixels.data[targetIndex + 1], pixels.data[targetIndex + 2]],
        count: 0,
      };
      entry.count += 1;
      targetCounts.set(targetKey, entry);
    }
  }

  const paletteMap = new Map(seedMap);
  for (const [sourceKey, targetCounts] of replacementCounts.entries()) {
    let best = null;
    for (const entry of targetCounts.values()) {
      if (!best || entry.count > best.count) {
        best = entry;
      }
    }
    if (best && best.count >= 2) {
      paletteMap.set(sourceKey, best.color);
    }
  }
  return paletteMap;
}

function buildPaletteMapFromColorRuns(baseRuns, targetRuns) {
  const paletteMap = new Map();
  const count = Math.min(baseRuns.length, targetRuns.length);
  for (let index = 0; index < count; index += 1) {
    const source = baseRuns[index];
    const target = targetRuns[index];
    if (!source || !target || paletteMap.has(source.key)) {
      continue;
    }
    paletteMap.set(source.key, target.color);
  }
  return paletteMap;
}

function getDominantPaletteColorKeyInRect(sheet, pixels, startX, startY, width, height) {
  const counts = new Map();
  for (let y = startY; y < startY + height; y += 1) {
    for (let x = startX; x < startX + width; x += 1) {
      if (!isPaletteSpritePixel(sheet, pixels, x, y)) {
        continue;
      }
      const index = (y * pixels.width + x) * 4;
      const key = rgbKey(pixels.data[index], pixels.data[index + 1], pixels.data[index + 2]);
      counts.set(key, (counts.get(key) ?? 0) + 1);
    }
  }
  return getDominantPaletteColorKey(counts);
}

function getDominantPaletteColorKey(counts) {
  let bestKey = null;
  let bestCount = 0;
  for (const [key, count] of counts.entries()) {
    if (count > bestCount) {
      bestKey = key;
      bestCount = count;
    }
  }
  return bestCount >= 2 ? bestKey : null;
}

function countAssetBackgroundPixelsInRect(sheet, pixels, startX, startY, width, height) {
  let count = 0;
  for (let y = startY; y < startY + height; y += 1) {
    for (let x = startX; x < startX + width; x += 1) {
      if (isAssetCellBackgroundPixel(sheet, pixels, x, y)) {
        count += 1;
      }
    }
  }
  return count;
}

function countSpritePixelsInRect(sheet, pixels, startX, startY, width, height) {
  let count = 0;
  for (let y = startY; y < startY + height; y += 1) {
    for (let x = startX; x < startX + width; x += 1) {
      if (isSpritePixel(sheet, pixels, x, y)) {
        count += 1;
      }
    }
  }
  return count;
}

function stripHasExtensionBackground(sheet, pixels, startX, startY, width, height) {
  for (let y = startY; y < startY + height; y += 1) {
    for (let x = startX; x < startX + width; x += 1) {
      if (isExtensionBackgroundPixel(sheet, pixels, x, y)) {
        return true;
      }
    }
  }
  return false;
}

function stripHasSpritePixels(sheet, pixels, startX, startY, width, height) {
  for (let y = startY; y < startY + height; y += 1) {
    for (let x = startX; x < startX + width; x += 1) {
      if (isSpritePixel(sheet, pixels, x, y)) {
        return true;
      }
    }
  }
  return false;
}

function isAssetCellBackgroundPixel(sheet, pixels, x, y) {
  if (!sheet.cellBackground || x < 0 || y < 0 || x >= pixels.width || y >= pixels.height) {
    return false;
  }
  const index = (y * pixels.width + x) * 4;
  return isNearColor(pixels.data, index, sheet.cellBackground);
}

function isExtensionBackgroundPixel(sheet, pixels, x, y) {
  if (!sheet.extensionBackground || x < 0 || y < 0 || x >= pixels.width || y >= pixels.height) {
    return false;
  }
  const index = (y * pixels.width + x) * 4;
  return isNearColor(pixels.data, index, sheet.extensionBackground);
}

function isSheetBackgroundPixel(sheet, pixels, x, y) {
  if (!sheet.sheetBackground || x < 0 || y < 0 || x >= pixels.width || y >= pixels.height) {
    return false;
  }
  const index = (y * pixels.width + x) * 4;
  return isNearColor(pixels.data, index, sheet.sheetBackground);
}

function isSpritePixel(sheet, pixels, x, y) {
  if (x < 0 || y < 0 || x >= pixels.width || y >= pixels.height) {
    return false;
  }
  const index = (y * pixels.width + x) * 4;
  return (
    pixels.data[index + 3] > 0 &&
    !isAssetCellBackgroundPixel(sheet, pixels, x, y) &&
    !isExtensionBackgroundPixel(sheet, pixels, x, y) &&
    !isSheetBackgroundPixel(sheet, pixels, x, y)
  );
}

function isPaletteSpritePixel(sheet, pixels, x, y) {
  return isSpritePixel(sheet, pixels, x, y);
}

function isTransparentAssetPixel(sheet, data, index) {
  return [sheet.cellBackground, sheet.extensionBackground, sheet.sheetBackground].some(
    (color) => color && isNearColor(data, index, color),
  );
}

function isNearColor(data, index, color, tolerance = 3) {
  const [red, green, blue] = color;
  return (
    Math.abs(data[index] - red) <= tolerance &&
    Math.abs(data[index + 1] - green) <= tolerance &&
    Math.abs(data[index + 2] - blue) <= tolerance
  );
}

function rgbKey(red, green, blue) {
  return `${red},${green},${blue}`;
}

function parseRgbKey(key) {
  return key.split(',').map((part) => Number.parseInt(part, 10));
}

